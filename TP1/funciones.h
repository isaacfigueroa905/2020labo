#include <stdio.h>

/** \brief :Calcula la suma de dos numeros
 *
 * \param float :el primer numero ingresado por el usuario al que se le sumara un segundo numero
 * \param float :el segundo numero ingresado por el usuario que se le sumara al primero
 * \return float :el resultado de sumar ambos numeros
 *
 */

float SumarNumeros(float, float);


/** \brief :Calcula la resta de dos numeros
 *
 * \param float :el primer numero ingresado por el usuario al que se le restara un segundo numero
 * \param float :el segundo numero ingresado por el usuario que se le restara al primero
 * \return float :el resultado de restar ambos numeros
 *
 */
 float RestarNumeros(float, float);


/** \brief :Calcula la multiplicacion de dos numeros
 *
 * \param :float el primer numero ingresado por el usuario al que se le multiplicara un segundo numero
 * \param :float el segundo numero ingresado por el usuario por el cual se multiplicara al primero
 * \return :float el resultado de multiplicar ambos numeros
 *
 */
float MultiplicarNumeros(float,float);



/** \brief :calcula la division de dos numeros
 *
 * \param float :el primer numero ingresado por el usuario, que sera el dividendo.
 * \param float :el segundo numero ingresado por el usuario, que sera el divisor.
 * \return float :el cociente de la division.
 *
 */
float DividirNumeros(float,float);




/** \brief :Calcula el factorial de un numero
 *
 * \param long int :el numero ingresado por el usuario sobre el cual se calculara el factorial.
 * \return long int :el resultado del calcula factorial.
 *
 */
long int  CalcularFactorial(long int);
